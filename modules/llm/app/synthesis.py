PARSE_FAILED_SUMMARY = (
    "Synthesis parse failed; see outputs/llm_generation.json for raw model text."
)

_EMPTY_PEER_REVIEW = {
    "strengths": [],
    "weaknesses": [],
    "missing_evidence": [],
    "suggested_improvements": [],
}


def _coerce_str_list(value: Any) -> list[str]:
    if isinstance(value, list):
        return [str(item).strip() for item in value if str(item).strip()]
    if isinstance(value, str) and value.strip():
        return [value.strip()]
    return []


def _extract_json_object(text: str) -> dict[str, Any] | None:
    text = text.strip()
    if not text:
        return None
    try:
        parsed = json.loads(text)
        return parsed if isinstance(parsed, dict) else None
    except json.JSONDecodeError:
        pass
    start = text.find("{")
    if start < 0:
        return None
    depth = 0
    for idx in range(start, len(text)):
        ch = text[idx]
        if ch == "{":
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth == 0:
                try:
                    parsed = json.loads(text[start : idx + 1])
                    return parsed if isinstance(parsed, dict) else None
                except json.JSONDecodeError:
                    return None
    return None


def _coerce_findings(value: Any) -> list[str]:
    if isinstance(value, dict):
        return [str(v).strip() for v in value.values() if str(v).strip()]
    return _coerce_str_list(value)


def _markdown_heading(line: str) -> tuple[str, int] | None:
    """Return normalized title and level for ATX or bold-only headings."""
    stripped = line.strip()
    atx = re.fullmatch(r"(#{1,6})\s+(.+?)\s*#*", stripped)
    if atx:
        title = re.sub(r"[*_`]", "", atx.group(2)).strip().rstrip(":").lower()
        return title, len(atx.group(1))
    bold = re.fullmatch(r"\*\*(.+?)\*\*\s*:?", stripped)
    if bold:
        title = re.sub(r"[*_`]", "", bold.group(1)).strip().rstrip(":").lower()
        return title, 2
    return None


def _markdown_heading_title(line: str) -> str | None:
    heading = _markdown_heading(line)
    return heading[0] if heading else None


def _section_text(text: str, heading_keywords: tuple[str, ...]) -> str:
    """Extract body text under the first Markdown heading matching a keyword."""
    lines = text.splitlines()
    collected: list[str] = []
    in_section = False
    section_level = 0
    for line in lines:
        heading = _markdown_heading(line)
        if heading is not None:
            title, level = heading
            if any(keyword in title for keyword in heading_keywords):
                in_section = True
                section_level = level
                continue
            if in_section and level <= section_level:
                break
            if in_section:
                collected.append(line)
            continue
        if in_section:
            collected.append(line)
    return "\n".join(collected).strip()


def _bullet_lines(block: str) -> list[str]:
    items: list[str] = []
    for line in block.splitlines():
        stripped = line.strip()
        if stripped.startswith(("-", "*")) and len(stripped) > 2:
            items.append(stripped.lstrip("-* ").strip())
    return items


def _section_items(
    text: str,
    heading_keywords: tuple[str, ...],
    *,
    label: str,
) -> list[str]:
    block = _section_text(text, heading_keywords)
    if not block:
        return []
    bullets = _bullet_lines(block)
    if bullets:
        return [re.sub(r"\s+", " ", item)[:1000] for item in bullets[:8]]
    compact = re.sub(r"\s+", " ", block).strip()
    return [f"{label}: {compact[:1000]}"] if compact else []


def parse_paper_summary(result: GenerationResult | None) -> dict[str, Any]:
    """Map a paper-only generation into the integration summary contract."""
    if result is None:
        return {
            "summary": "Generation failed: no generation result was returned.",
            "key_findings": [],
            "research_gaps": [],
        }
    generated_text = _strip_outer_markdown_fence(_generated_text(result))
    if result.error:
        return {
            "summary": generated_text,
            "key_findings": [],
            "research_gaps": [],
        }
    key_findings = [
        *_section_items(
            generated_text,
            ("core contribution", "contribution"),
            label="Core contribution",
        ),
        *_section_items(generated_text, ("results", "findings"), label="Results"),
    ]
    research_gaps = _section_items(
        generated_text,
        ("limitations", "research gaps", "missing evidence"),
        label="Limitations",
    )
    return {
        "summary": generated_text,
        "key_findings": key_findings[:12],
        "research_gaps": research_gaps[:12],
    }


def parse_paper_review(result: GenerationResult | None) -> dict[str, str]:
    """Map a paper-only review generation into a stable integration payload."""
    if result is None:
        return {"peer_review": "Generation failed: no generation result was returned."}
    return {"peer_review": _strip_outer_markdown_fence(_generated_text(result))}


def _fields_from_markdown_fallback(text: str) -> dict[str, Any] | None:
    """Best-effort mapping when the model returns prose instead of JSON."""
    stripped = text.strip()
    if len(stripped) < 80:
        return None
    has_heading = any(
        _markdown_heading_title(line) is not None for line in stripped.splitlines()
    )
    if not has_heading and "\n-" not in stripped:
        if stripped.startswith(("{", "[")):
            return None
        return {
            "summary": re.sub(r"\s+", " ", stripped),
            "key_findings": [],
            "research_gaps": [],
        }
    conclusion = _section_text(text, ("conclusion", "summary"))
    findings_block = _section_text(text, ("findings", "key findings"))
    gaps_block = _section_text(
        text,
        ("assumptions", "research gaps", "gaps", "limitations"),
    )

    key_findings = _bullet_lines(findings_block)
    if findings_block and not key_findings:
        key_findings = [
            re.sub(r"\s+", " ", chunk).strip()
            for chunk in re.split(r"\n#{3,6}\s+", findings_block)
            if chunk.strip()
        ]

    research_gaps = _bullet_lines(gaps_block)
    summary = conclusion
    if not summary:
        leading_lines: list[str] = []
        for line in stripped.splitlines():
            if _markdown_heading_title(line) is not None:
                break
            if line.strip().startswith(("-", "*")):
                break
            leading_lines.append(line)
        summary = re.sub(r"\s+", " ", "\n".join(leading_lines)).strip()
    if not summary and not key_findings and not research_gaps:
        return None
    return {
        "summary": summary,
        "key_findings": key_findings[:12],
        "research_gaps": research_gaps[:12],
    }


def _fields_from_parsed(obj: dict[str, Any]) -> dict[str, Any]:
    summary = obj.get("summary") or obj.get("abstract") or ""
    summary = str(summary).strip() if summary is not None else ""
    key_findings = _coerce_findings(obj.get("key_findings") or obj.get("findings"))
    research_gaps = _coerce_str_list(obj.get("research_gaps") or obj.get("gaps"))
    if not summary and key_findings:
        summary = key_findings[0]
    return {
        "summary": summary,
        "key_findings": key_findings,
        "research_gaps": research_gaps,
    }


def parse_topic_synthesis(result: GenerationResult | None) -> dict[str, Any]:
    """Parse model JSON into summary / key_findings / research_gaps for topic mode."""
    failed = {
        "summary": PARSE_FAILED_SUMMARY,
        "key_findings": [],
        "research_gaps": [],
    }
    if result is None or not result.text.strip():
        if result and result.error:
            failed["summary"] = f"Synthesis failed: {result.error}"
        return failed
    if result.error:
        return {
            "summary": f"Synthesis failed: {result.error}",
            "key_findings": [],
            "research_gaps": [],
        }
    parsed_obj = _extract_json_object(result.text)
    if parsed_obj:
        fields = _fields_from_parsed(parsed_obj)
    else:
        fallback = _fields_from_markdown_fallback(result.text)
        if not fallback:
            return failed
        fields = fallback
    if not fields["summary"] and not fields["key_findings"] and not fields["research_gaps"]:
        return failed
    if not fields["summary"]:
        fields["summary"] = PARSE_FAILED_SUMMARY
    return fields


def analysis_result_from_pack(pack: dict[str, Any], result: GenerationResult | None = None) -> dict[str, Any]:
    validate_rag_evidence_pack(pack)
    synthesis = parse_topic_synthesis(result)
    result_obj = {
        "input_type": "topic_text",
        "paper": None,
        "query": pack["query"],
        "summary": synthesis["summary"],
        "key_findings": synthesis["key_findings"],
        "research_gaps": synthesis["research_gaps"],
        "recommendations": pack["candidates"],
        "peer_review": dict(_EMPTY_PEER_REVIEW),
        "evidence_sources": [snippet["source_id"] for snippet in pack["evidence_snippets"]],
    }
    validate_analysis_result(result_obj)
    return result_obj


def _paper_year(paper: dict[str, Any]) -> str | None:
    date = paper.get("published_date") or paper.get("update_date") or ""
    if isinstance(date, str) and len(date) >= 4:
        return date[:4]
    return None


def _snippet_for_paper(
    paper_id: str,
    evidence_pack: dict[str, Any],
    paper: dict[str, Any],
) -> str:
    for snippet in evidence_pack.get("evidence_snippets", []):
        if snippet.get("source_id") == paper_id:
            text = str(snippet.get("snippet") or "").strip()
            if text:
                return text
    abstract = str(paper.get("abstract") or "").strip()
    if len(abstract) > 300:
        return abstract[:300].rstrip() + "..."
    return abstract


def parse_paper_recommendation_summaries(
    result: GenerationResult | None,
) -> dict[str, str]:
    """Parse source_id -> summary mappings from model output."""
    summaries: dict[str, str] = {}
    if result is None or result.error or not result.text.strip():
        return summaries
    parsed_obj = _extract_json_object(result.text)
    if not parsed_obj:
        return summaries
    if isinstance(parsed_obj.get("paper_summaries"), list):
        for item in parsed_obj["paper_summaries"]:
            if not isinstance(item, dict):
                continue
            source_id = str(item.get("source_id") or "").strip()
            summary = str(item.get("summary") or "").strip()
            if source_id and summary and source_id not in summaries:
                summaries[source_id] = summary
    elif isinstance(parsed_obj.get("summaries"), dict):
        for source_id, summary in parsed_obj["summaries"].items():
            sid = str(source_id).strip()
            text = str(summary).strip()
            if sid and text and sid not in summaries:
                summaries[sid] = text
    return summaries


def resolve_paper_summary(
    paper_id: str,
    summaries: dict[str, str],
    evidence_pack: dict[str, Any],
    paper: dict[str, Any],
) -> str:
    """Use a validated LLM summary or fall back to Bank's abstract snippet."""
    summary = summaries.get(paper_id, "").strip()
    if summary:
        return summary
    return _snippet_for_paper(paper_id, evidence_pack, paper)


def build_recommended_paper_cards(
    raw_recommendations: list[dict[str, Any]],
    summaries: dict[str, str],
    evidence_pack: dict[str, Any],
    *,
    max_papers: int = 5,
) -> list[dict[str, Any]]:
    """Merge Bank metadata with validated per-paper summaries."""
    valid_ids = {
        str(snippet.get("source_id") or "")
        for snippet in evidence_pack.get("evidence_snippets", [])
        if snippet.get("source_id")
    }
    selected = raw_recommendations[:max_papers]
    allowed_ids = {
        str((rec.get("paper") or {}).get("paper_id") or (rec.get("paper") or {}).get("id") or "")
        for rec in selected
    }
    filtered_summaries = {
        source_id: text
        for source_id, text in summaries.items()
        if source_id in allowed_ids and source_id in valid_ids
    }

    cards: list[dict[str, Any]] = []
    for rec in selected:
        paper = rec.get("paper") or {}
        paper_id = str(paper.get("paper_id") or paper.get("id") or "")
        authors = paper.get("authors", [])
        if not isinstance(authors, list):
            authors = [str(authors)] if authors else []
        cards.append(
            {
                "paper_id": paper_id,
                "title": paper.get("title", ""),
                "authors": authors,
                "year": _paper_year(paper),
                "url": paper.get("url", ""),
                "score": float(rec.get("score", 0.0)),
                "apa_citation": rec.get("apa_citation", ""),
                "summary": resolve_paper_summary(
                    paper_id,
                    filtered_summaries,
                    evidence_pack,
                    paper,
                ),
            }
        )
    return cards


