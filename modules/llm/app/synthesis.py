def _source_lookup(pack: dict[str, Any]) -> dict[str, dict[str, Any]]:
    return {snippet["source_id"]: snippet for snippet in pack["evidence_snippets"]}


def _generated_text(result: GenerationResult) -> str:
    """Return backend text or an explicit failure message without fake fallback output."""
    if result.error:
        return f"Generation failed: {result.error}"
    if result.text.strip():
        return result.text.strip()
    return "Generation failed: the backend returned no text."


def _strip_outer_markdown_fence(text: str) -> str:
    """Remove a single outer Markdown fence while preserving inner content."""
    stripped = text.strip()
    match = re.fullmatch(
        r"```(?:markdown|md)?\s*\n(?P<body>.*)\n```",
        stripped,
        flags=re.DOTALL | re.IGNORECASE,
    )
    return match.group("body").strip() if match else stripped


def _verification_lines(audit: dict[str, Any] | None) -> list[str]:
    if not audit or not audit.get("verification_used"):
        return []
    lines = [
        "",
        "## LLM Verification Pass",
        "",
        f"- Verification revised the draft: **{audit.get('verification_revised', False)}**.",
        f"- Verifier supported flag: **{audit.get('supported', False)}**.",
    ]
    unsupported = audit.get("unsupported_claims") or []
    if unsupported:
        lines.append(
            "- Unsupported claims flagged: "
            + "; ".join(str(item) for item in unsupported[:8])
        )
    if audit.get("error"):
        lines.append(f"- Verifier error: {audit['error']}.")
    lines.append(
        "- Same-model verification; combine with deterministic audit and human spot checks."
    )
    lines.append("")
    return lines


def synthesize_markdown(
    pack: dict[str, Any],
    style: str,
    result: GenerationResult,
    *,
    verification_audit: dict[str, Any] | None = None,
) -> str:
    """Render a RAG synthesis and verification report."""
    validate_rag_evidence_pack(pack)
    snippets = _source_lookup(pack)
    source_list = ", ".join(sorted(snippets))
    recommendations = pack["candidates"]
    generated_text = _strip_outer_markdown_fence(_generated_text(result))
    backend_line = (
        f"**Generation backend:** {result.backend}  \n"
        f"**Model:** {result.model}  \n"
        f"**Latency:** {result.latency_seconds}s  \n"
        f"**Generation error:** {result.error or 'none'}"
    )

    lines = [
        "# LLM Synthesis",
        "",
        "**AI disclosure:** This narrative is AI-generated by the local LLM module (`modules/llm`). "
        "It is constrained to the supplied `RagEvidencePack`; unsupported claims are marked "
        "as assumptions or omitted.",
        "",
        f"**Query:** {pack['query']}",
        f"**Retrieval mode:** {pack['retrieval_mode']}",
        f"**Style:** {style}",
    ]
    lines.extend(["", backend_line])
    lines.extend(
        [
            "",
            "## Generated Analysis",
            "",
            generated_text,
            "",
            "## Recommendations From Metadata",
            "",
            "| Rank | Relation | Paper | Evidence | Why it matters | APA citation |",
            "| --- | --- | --- | --- | --- | --- |",
        ]
    )
    for rank, recommendation in enumerate(recommendations, start=1):
        evidence = ", ".join(recommendation["evidence"])
        paper = recommendation["paper"]
        lines.append(
            f"| {rank} | {recommendation['relation']} | {paper['title']} | {evidence} | "
            f"{recommendation['reason']} | {recommendation['apa_citation']} |"
        )

    verification = check_generation(generated_text, pack)
    lines.extend(
        [
            "",
            "## Deterministic Verification Audit",
            "",
            f"- Source IDs available in evidence pack: {source_list}.",
            f"- Source IDs used by generated answer: {', '.join(verification['used_source_ids']) or 'none'}.",
            f"- Unsupported source IDs: {', '.join(verification['unsupported_source_ids']) or 'none'}.",
            "- APA strings in the recommendation table were copied from `Recommendation.apa_citation`; no DOI or venue was invented by this renderer.",
        ]
    )
    lines.extend(_verification_lines(verification_audit))
    lines.append("")
    return "\n".join(lines)


def summarize_paper_markdown(
    paper: dict[str, Any],
    style: str,
    result: GenerationResult,
    *,
    verification_audit: dict[str, Any] | None = None,
) -> str:
    """Render a paper-only summary with an explicit no-retrieval disclosure."""
    validate_parsed_paper(paper)
    metadata = paper["metadata"]
    sections = paper["sections"]
    generated_text = _strip_outer_markdown_fence(_generated_text(result))
    backend_lines = [
        f"**Generation backend:** {result.backend}  ",
        f"**Model:** {result.model}  ",
        f"**Latency:** {result.latency_seconds}s  ",
        f"**Generation error:** {result.error or 'none'}",
    ]

    lines = [
        "# Paper Summary",
        "",
        "**AI disclosure:** This summary is AI-generated from the supplied `ParsedPaper` only.",
        "",
        "**Evidence scope:** No external evidence or retrieval was used. No `RagEvidencePack` "
        "was created or loaded.",
        "",
        f"**Paper:** {metadata['title']}",
        f"**Style:** {style}",
    ]
    lines.extend(["", *backend_lines])
    lines.extend(["", "## Generated Summary", "", generated_text, ""])
    lines.extend(_verification_lines(verification_audit))
    if not sections.get("abstract") and not metadata.get("abstract"):
        lines.extend(
            [
                "## Input Warning",
                "",
                "The supplied ParsedPaper does not contain abstract text.",
                "",
            ]
        )
    return "\n".join(lines)


